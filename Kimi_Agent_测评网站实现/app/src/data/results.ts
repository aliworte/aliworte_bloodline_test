// 结果查找函数
export function getResultText(bloodlineType: string, bloodlineRaces: string[], blessing: string | null): string {
  const order: Record<string, number> = { elf: 0, beast: 1, mer: 2, ghost: 3, dwarf: 4, human: 5 };
  const sorted = [...bloodlineRaces].sort((a, b) => (order[a] ?? 99) - (order[b] ?? 99));

  let key: string;

  if (bloodlineType === '纯血') {
    key = `pure_${sorted[0]}`;
  } else if (bloodlineType === '双血脉') {
    key = `dual_${sorted.join('_')}_${blessing}`;
  } else if (bloodlineType === '三血脉') {
    key = `tri_${sorted.join('_')}_${blessing}`;
  } else {
    key = 'chaos';
  }

  return RESULTS[key] || RESULTS['chaos'];
}

// 获取结果标题
export function getResultTitle(bloodlineType: string, bloodlineRaces: string[], blessing: string | null): string {
  const raceNames: Record<string, string> = {
    elf: '阿莎娜',
    beast: '玛瑞维',
    mer: '蒙佩洛',
    ghost: '迪蒙特',
    dwarf: '多瑞斯',
    human: '伊亚迪克',
  };

  const order: Record<string, number> = { elf: 0, beast: 1, mer: 2, ghost: 3, dwarf: 4, human: 5 };
  const sorted = [...bloodlineRaces].sort((a, b) => (order[a] ?? 99) - (order[b] ?? 99));

  if (bloodlineType === '纯血') {
    const race = sorted[0];
    const displayMap: Record<string, string> = {
      elf: '精灵', beast: '兽人', mer: '人鱼', ghost: '鬼族', dwarf: '矮人', human: '人类',
    };
    return `${raceNames[race]}（${displayMap[race]}）`;
  } else if (bloodlineType === '双血脉') {
    const raceNamesList = sorted.map(r => raceNames[r]);
    return `${raceNamesList.join('·')}（${blessing ? raceNames[blessing] : ''}赐福）`;
  } else if (bloodlineType === '三血脉') {
    const raceNamesList = sorted.map(r => raceNames[r]);
    return `${raceNamesList.join('·')}（${blessing ? raceNames[blessing] : ''}赐福）`;
  } else {
    return '混沌未分化';
  }
}

// 获取结果子标题（混血种族名）
export function getResultSubtitle(bloodlineType: string, bloodlineRaces: string[], blessing: string | null): string {
  const raceNames: Record<string, string> = {
    elf: '阿莎娜',
    beast: '玛瑞维',
    mer: '蒙佩洛',
    ghost: '迪蒙特',
    dwarf: '多瑞斯',
    human: '伊亚迪克',
  };

  if (bloodlineType === '纯血') {
    return `你在另一个世界的种族是：${raceNames[bloodlineRaces[0]]}`;
  } else if (bloodlineType === '双血脉' || bloodlineType === '三血脉') {
    if (blessing) {
      return `你在另一个世界的种族是：混血${raceNames[blessing]}`;
    }
  }
  return '你的血脉测试结果';
}
